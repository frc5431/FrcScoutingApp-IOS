- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"MainCell"];
    
    if(cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"MainCell"];
    }
    
    NSArray *items;
    items = [NSArray arrayWithObjects:
          @"Team 4576 match 2 Report",
          @"Team 3476 match 2 Report",
          @"Team 6785 match 2 Report",
          @"Team 5431 match 3 Report",
          @"Team 2356 match 3 Report",
          @"Team 4598 match 3 Report",
          @"Team 5632 match 4 Report",
          @"Team 1254 match 4 Report",
          @"Team 1276 match 4 Report",
             nil];
    
    cell.textLabel.text = [items objectAtIndex:indexPath.row];
    
    
    return cell;
}

